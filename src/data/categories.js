function expandFields(groups, customFields) {
  const fields = [];
  for (const g of groups || []) {
    const group = FIELD_GROUPS[g];
    if (group) fields.push(...group);
  }
  for (const c of customFields || []) {
    fields.push(c);
  }
  return fields;
}

